$(window).scroll(
    function(){
        if($(window).width()>767){
        if($("#navbar").offset().top>800){
            $("#navbar").addClass("negro")
        }
        else{
            $("#navbar").removeClass("negro")
        }}
        else{
            if($("#navbar").offset().top>350){
                $("#navbar").addClass("negro")
            }
            else{
                $("#navbar").removeClass("negro")
        }
    }}
)